/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  uint32_t raw;
  uint32_t buffer[buff_size];
  uint32_t filter;
  uint32_t sum = 0;
  uint32_t i = 0;

  for (int i = 0; i < buff_size; i++) {
    buffer[i] = 0;
  }

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  /* USER CODE BEGIN 2 */

  HAL_GPIO_WritePin(NONCRITICAL_DCDC, 0);
  HAL_GPIO_WritePin(NONCRITICAL_SUPP, 0); //Turn on Supplemental Connection - FUSE
  HAL_GPIO_WritePin(CRITICAL_DCDC, 0);
  HAL_GPIO_WritePin(CRITICAL_SUPP, 0); //Turn off Supplemental Connection - BPS

  int lv_en = 0;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
    for (int x = 0; x < 50; x++) {
      HAL_ADC_Start(&hadc1); // Starts the ADC conversion for the instance specified by hadc.
      //&hadc passes the address of adc we want to use (contains configs and info abt ADC)
      HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY); // This waits for conversion to complete
      raw = HAL_ADC_GetValue(&hadc1); // gets value
      filter = average_filter(raw, buffer, buff_size, &i, &sum);
    }

    lv_en = HAL_GPIO_ReadPin(LV_EN_PIN);
    HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, lv_en);

    if (lv_en) {
      //2895
      if (filter >= THRESHHOLD + 100) {
        // HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, 1);
        HAL_GPIO_WritePin(CRITICAL_SUPP, 0); //Turn off Supplemental Connection
        HAL_GPIO_WritePin(CRITICAL_DCDC, 1); //Turn on DCDC Connection
        HAL_GPIO_WritePin(NONCRITICAL_DCDC, 1);        
      }
      else if (filter < THRESHHOLD - 700){
        // HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, 0);
        HAL_GPIO_WritePin(CRITICAL_DCDC, 0); //Turn off DCDC Connection
        HAL_GPIO_WritePin(NONCRITICAL_DCDC, 0);
        HAL_GPIO_WritePin(CRITICAL_SUPP, 1); //Turn on Supplemental Connection
      }
    } else {
      HAL_GPIO_WritePin(CRITICAL_DCDC, 0);
      HAL_GPIO_WritePin(CRITICAL_SUPP, 0); //Turn on Supplemental Connection - FUSE
      HAL_GPIO_WritePin(NONCRITICAL_DCDC, 0);
      HAL_GPIO_WritePin(NONCRITICAL_SUPP, 0); //Turn off Supplemental Connection - BPS
    }

    HAL_Delay(50);

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

